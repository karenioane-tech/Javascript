// Helper exports for Lesson 14

export const appName = "JavaScript Basics";

export function add(a, b) {
  return a + b;
}

export function subtract(a, b) {
  return a - b;
}

export function formatUser(name, role) {
  return `${name} is a ${role}.`;
}

export default function greet(name) {
  return `Hello, ${name}!`;
}

